    <section class="manifest-body alma-content-section" aria-label="Política AlmaDesign">
        <article class="manifest-section" id="politica-introduccion">
            <p class="manifest-section__number"><em>01</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Marco general</p>
                <h2>Política de AlmaDesign</h2>
                <p>La inteligencia artificial no debe reemplazar el criterio, la dignidad ni la voluntad de las personas. Debe abrir espacio para pensar mejor, decidir con más claridad, trabajar con menos fricción y crear con mayor libertad.</p>
                <p>Por eso, nuestro compromiso es diseñar soluciones digitales, asistentes inteligentes, sistemas RAG, automatizaciones y experiencias conversacionales bajo una idea central.</p>
                <blockquote>
                    <p>La IA debe estar al servicio de las personas, no las personas al servicio de la IA.</p>
                </blockquote>
            </div>
        </article>

        <article class="manifest-section" id="politica-compromiso">
            <p class="manifest-section__number"><em>02</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Compromiso principal</p>
                <h2>Respetar privacidad, datos y criterio humano.</h2>
                <p>AlmaDesign se compromete a desarrollar soluciones tecnológicas que respeten:</p>
                <ul>
                    <li>la privacidad de las personas;</li>
                    <li>la protección de sus datos personales;</li>
                    <li>la transparencia en el uso de inteligencia artificial;</li>
                    <li>el consentimiento informado;</li>
                    <li>la seguridad de la información;</li>
                    <li>la trazabilidad de los procesos;</li>
                    <li>la intervención humana cuando una decisión pueda afectar de manera relevante a una persona.</li>
                </ul>
                <p>No usamos la IA para manipular, presionar, discriminar ni ocultar decisiones importantes detrás de sistemas automáticos.</p>
                <p>Usamos la IA para orientar, organizar conocimiento, mejorar procesos, crear nuevas capacidades y ayudar a que personas y equipos trabajen con más claridad.</p>
            </div>
        </article>

        <article class="manifest-section" id="politica-datos-personales">
            <p class="manifest-section__number"><em>03</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Datos personales</p>
                <h2>Mínimos, necesarios y con propósito.</h2>
                <p>AlmaDesign solo solicita datos personales cuando son necesarios para una finalidad clara.</p>
                <p>Antes de pedir datos como nombre, correo, teléfono u otra información de contacto, procuramos explicar:</p>
                <ul>
                    <li>para qué se solicitan;</li>
                    <li>cómo serán utilizados;</li>
                    <li>quién podrá recibirlos;</li>
                    <li>durante cuánto tiempo podrían ser conservados;</li>
                    <li>qué derechos tiene la persona respecto de ellos.</li>
                </ul>
                <blockquote>
                    <p>Primero orientamos. Solo pedimos datos cuando la persona decide avanzar.</p>
                </blockquote>
                <p>En experiencias conversacionales, formularios o asistentes IA, una persona puede explorar información general sin entregar datos personales al inicio.</p>
                <p>Si desea ser contactada, solicitar una propuesta, conversar con un ejecutivo o recibir atención personalizada, pediremos autorización antes de recopilar los datos mínimos necesarios.</p>
            </div>
        </article>

        <article class="manifest-section" id="politica-consentimiento">
            <p class="manifest-section__number"><em>04</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Consentimiento</p>
                <h2>Claro, libre y trazable.</h2>
                <p>Cuando sea necesario recopilar datos personales, AlmaDesign buscará obtener un consentimiento libre, informado, específico, previo e inequívoco.</p>
                <p>Esto significa que la persona debe entender qué está autorizando.</p>
                <p>Evitamos textos confusos, casillas premarcadas o mecanismos que fuercen una aceptación sin comprensión real.</p>
                <p>Cuando corresponda, registraremos evidencia del consentimiento, incluyendo:</p>
                <ul>
                    <li>fecha y hora;</li>
                    <li>finalidad declarada;</li>
                    <li>texto de consentimiento utilizado;</li>
                    <li>canal o formulario de origen;</li>
                    <li>datos entregados;</li>
                    <li>autorización para contacto humano, si aplica.</li>
                </ul>
                <p>El consentimiento no es una formalidad. Es una señal de respeto.</p>
            </div>
        </article>

        <article class="manifest-section" id="politica-uso-ia">
            <p class="manifest-section__number"><em>05</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Uso responsable</p>
                <h2>La IA apoya procesos, no suplanta confianza.</h2>
                <p>AlmaDesign utiliza inteligencia artificial para apoyar procesos como:</p>
                <ul>
                    <li>orientación inicial;</li>
                    <li>búsqueda de información en documentos;</li>
                    <li>asistentes conversacionales;</li>
                    <li>automatización de tareas;</li>
                    <li>generación de resúmenes;</li>
                    <li>apoyo a equipos comerciales, técnicos o administrativos;</li>
                    <li>estructuración de conocimiento;</li>
                    <li>análisis de información interna o pública autorizada.</li>
                </ul>
                <p>Cuando una persona interactúa con un sistema basado en IA, procuramos que sea claro que está usando una herramienta automatizada o asistida por IA.</p>
                <p>La IA puede ayudar, pero no debe simular falsamente ser una persona cuando eso pueda afectar la confianza del usuario.</p>
            </div>
        </article>

        <article class="manifest-section" id="politica-decisiones-sensibles">
            <p class="manifest-section__number"><em>06</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Decisiones sensibles</p>
                <h2>La IA no decide sola lo importante.</h2>
                <p>AlmaDesign no promueve que sistemas de IA tomen decisiones automáticas sensibles sin revisión humana adecuada.</p>
                <p>Especialmente en contextos relacionados con salud, seguros, finanzas, empleo, educación, consumidores o derechos de las personas, recomendamos mantener intervención humana significativa.</p>
                <p>La IA puede sugerir, resumir, ordenar o explicar. Pero las decisiones relevantes deben conservar responsabilidad humana.</p>
                <blockquote>
                    <p>La IA puede ampliar el juicio humano, pero no debe borrar la responsabilidad humana.</p>
                </blockquote>
            </div>
        </article>

        <article class="manifest-section" id="politica-transparencia">
            <p class="manifest-section__number"><em>07</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Transparencia</p>
                <h2>Explicar fuentes, límites y evidencia.</h2>
                <p>Nos esforzamos por construir sistemas que puedan explicar de dónde proviene una respuesta, especialmente cuando usan documentos, bases de conocimiento o sistemas RAG.</p>
                <p>Cuando una respuesta se basa en información disponible, buscamos que sea:</p>
                <ul>
                    <li>clara;</li>
                    <li>verificable;</li>
                    <li>limitada a las fuentes disponibles;</li>
                    <li>honesta cuando no existe información suficiente.</li>
                </ul>
                <p>No diseñamos sistemas para inventar precios, condiciones, beneficios, diagnósticos o promesas comerciales sin respaldo.</p>
                <p>Si una respuesta requiere validación humana, debe decirlo.</p>
            </div>
        </article>

        <article class="manifest-section" id="politica-seguridad">
            <p class="manifest-section__number"><em>08</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Seguridad</p>
                <h2>Confidencialidad como práctica continua.</h2>
                <p>AlmaDesign trata los datos personales y la información de clientes como activos sensibles.</p>
                <p>Aplicamos medidas razonables para proteger la información contra:</p>
                <ul>
                    <li>acceso no autorizado;</li>
                    <li>pérdida;</li>
                    <li>filtración;</li>
                    <li>uso indebido;</li>
                    <li>modificación no autorizada;</li>
                    <li>exposición innecesaria.</li>
                </ul>
                <p>El acceso a datos personales debe limitarse a quienes realmente lo necesitan para cumplir la finalidad declarada.</p>
                <p>La seguridad no es solo una capa técnica. Es una práctica continua de responsabilidad.</p>
            </div>
        </article>

        <article class="manifest-section" id="politica-fines">
            <p class="manifest-section__number"><em>09</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Finalidad</p>
                <h2>No usamos datos para fines ocultos.</h2>
                <p>Los datos entregados para una finalidad específica no deben reutilizarse para otra finalidad incompatible sin una nueva base legítima o autorización correspondiente.</p>
                <p>Por ejemplo, si una persona entrega sus datos para ser contactada por una consulta específica, esos datos no deben transformarse automáticamente en una base comercial amplia sin información clara y opción de oposición.</p>
                <p>AlmaDesign promueve sistemas donde cada dato tenga:</p>
                <ul>
                    <li>origen claro;</li>
                    <li>finalidad clara;</li>
                    <li>responsable claro;</li>
                    <li>trazabilidad clara.</li>
                </ul>
            </div>
        </article>

        <article class="manifest-section" id="politica-oposicion">
            <p class="manifest-section__number"><em>10</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Contacto responsable</p>
                <h2>Respetar voluntad, momento y confianza.</h2>
                <p>Las personas deben poder oponerse al uso de sus datos para fines comerciales o de marketing directo cuando corresponda.</p>
                <p>AlmaDesign recomienda que toda solución de captación, contacto o seguimiento comercial incluya mecanismos para:</p>
                <ul>
                    <li>solicitar baja;</li>
                    <li>corregir datos;</li>
                    <li>limitar contacto;</li>
                    <li>eliminar información cuando corresponda;</li>
                    <li>registrar solicitudes de oposición.</li>
                </ul>
                <p>Un buen sistema comercial no insiste a cualquier costo. Un buen sistema comercial respeta el momento, la voluntad y la confianza de las personas.</p>
            </div>
        </article>

        <article class="manifest-section" id="politica-rag">
            <p class="manifest-section__number"><em>11</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">RAG y asistentes</p>
                <h2>Privacidad desde el diseño.</h2>
                <p>En asistentes conversacionales, RAG y sistemas de captación de leads, AlmaDesign adopta un enfoque de privacidad desde el diseño.</p>
                <p>El flujo recomendado es:</p>
                <pre class="policy-flow"><code>persona explora de forma anónima
→ la IA orienta con información general
→ la persona manifiesta interés
→ se explica por qué se necesitan datos
→ se solicita consentimiento explícito
→ se recopilan datos mínimos
→ se deriva a una persona o equipo autorizado</code></pre>
                <p>Esto permite reemplazar prácticas opacas, como bases de datos compradas o contactos sin contexto, por relaciones más limpias:</p>
                <blockquote>
                    <p>leads propios, consentidos, trazables y contextualizados.</p>
                </blockquote>
            </div>
        </article>

        <article class="manifest-section" id="politica-datos-sensibles">
            <p class="manifest-section__number"><em>12</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Datos sensibles</p>
                <h2>Especial cuidado donde puede existir daño.</h2>
                <p>AlmaDesign reconoce que ciertos datos requieren una protección reforzada.</p>
                <p>Esto incluye, entre otros, información relacionada con salud, situación financiera, datos biométricos, menores de edad, creencias, origen, condiciones personales o cualquier información que pueda exponer a una persona a discriminación, daño o trato injusto.</p>
                <p>En estos casos, el diseño debe ser más cuidadoso:</p>
                <ul>
                    <li>pedir menos datos;</li>
                    <li>explicar mejor la finalidad;</li>
                    <li>evitar inferencias innecesarias;</li>
                    <li>mantener intervención humana;</li>
                    <li>limitar accesos;</li>
                    <li>registrar decisiones;</li>
                    <li>evitar automatizaciones que puedan afectar derechos sin revisión.</li>
                </ul>
            </div>
        </article>

        <article class="manifest-section" id="politica-sentido-humano">
            <p class="manifest-section__number"><em>13</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Sentido humano</p>
                <h2>La mejor IA hace mejor el trabajo humano.</h2>
                <p>Para AlmaDesign, una buena solución de IA no es la que parece más compleja. Es la que ayuda a una persona o equipo a vivir mejor su trabajo.</p>
                <p>Una IA bien diseñada puede:</p>
                <ul>
                    <li>devolver tiempo;</li>
                    <li>ordenar conocimiento disperso;</li>
                    <li>reducir tareas repetitivas;</li>
                    <li>mejorar decisiones;</li>
                    <li>preservar memoria institucional;</li>
                    <li>abrir nuevas posibilidades creativas;</li>
                    <li>acercar tecnología a personas que no son técnicas.</li>
                </ul>
                <p>Pero nunca debe hacer que la persona se sienta menos capaz, menos libre o menos importante.</p>
                <p>La tecnología debe ser puente, no muro.</p>
            </div>
        </article>

        <article class="manifest-section" id="politica-mejora-continua">
            <p class="manifest-section__number"><em>14</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Mejora continua</p>
                <h2>La responsabilidad también evoluciona.</h2>
                <p>La regulación sobre datos personales e inteligencia artificial seguirá evolucionando en Chile y en el mundo.</p>
                <p>AlmaDesign se compromete a revisar y mejorar sus prácticas conforme avancen:</p>
                <ul>
                    <li>la Ley 21.719 sobre Protección de Datos Personales;</li>
                    <li>la discusión legislativa chilena sobre inteligencia artificial;</li>
                    <li>las recomendaciones de buenas prácticas;</li>
                    <li>los aprendizajes obtenidos en proyectos reales;</li>
                    <li>las necesidades de clientes y usuarios.</li>
                </ul>
                <p>Esta política no busca ser una promesa legal absoluta. Busca declarar una forma de trabajo responsable.</p>
                <p>Cuando un proyecto lo requiera, AlmaDesign recomendará revisión jurídica especializada.</p>
            </div>
        </article>

        <article class="manifest-section" id="politica-declaracion-final">
            <p class="manifest-section__number"><em>15</em></p>
            <div class="manifest-section__content">
                <p class="manifest-section__subtitle">Declaración final</p>
                <h2>Una IA que hace más humano el futuro.</h2>
                <p>En AlmaDesign diseñamos tecnología para ampliar posibilidades humanas.</p>
                <p>Creemos en una inteligencia artificial transparente, útil, cuidadosa y comprensible.</p>
                <p>Una IA que no robe la voz de las personas, sino que les devuelva tiempo para usarla mejor.</p>
                <p>Una IA que no esconda decisiones, sino que ayude a tomarlas con más claridad.</p>
                <p>Una IA que no reduzca al ser humano a un dato, sino que reconozca en cada dato una responsabilidad.</p>
                <blockquote>
                    <p>La inteligencia artificial que vale la pena construir es aquella que hace más humano el futuro.</p>
                </blockquote>
            </div>
